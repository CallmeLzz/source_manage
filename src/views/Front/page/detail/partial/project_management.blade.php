            <h3 class="course-description-heading">Course Description</h3>
            <div>
                <p>This online course is aimed at people interested in IT project management and builds on other business and IT courses available online at .</p>
                <p>Every competent project manager needs to know the various phases and project management software and tools available do the job properly (such as the Gantt Chart). 
                    This free course will identify these phases and tools, enabling you to successfully manage any project.</p>
                <h4>LEARNING OUTCOMES</h4>
                <p class="justify">After completing this course you will understand the history of project management and learn from examples of interviews with project managers. 
                    You will gain a good&nbsp;<strong>knowledge</strong>&nbsp;of project management methodology. Each phase is examined: investigation, planning, design, production, monitoring and evaluation. 
                    You will be able to identify and understand the most important tools used by a project manager including GANTT charts, PERT Diagrams, estimating activity time, scheduling, expediting and Critical Path Method. 
                    You will gain a good&nbsp;<strong>knowledge</strong>&nbsp;of program, system, technical, user, paper-based, electronic and media project management documentation. This course will teach you the process of the 
                    systems development life cycle to help identify problems and create solutions in each stage of the projects life including planning, analysis, design, implementation and evaluation. 
                    You will learn about different issues that can occur when an information system is changed or a new sytem is introduced
                </p>
